            <h2>Для чего была введена программа утилизации автомобилей</h2>
            <p>
              Впервые в России проект реализовали в 2010 году и с каждым годом его продлевали. Утилизация автомобилей
              направлена на достижение сразу нескольких целей. Первая цель – это повышение безопасности дорожного
              движения, ведь на старых машинах очень небезопасно ездить. Вторая – стимулировать рынок отечественного
              автопрома и поддерживать отечественного производителя. Третья – улучшить экологическую обстановку в
              стране, во-первых, старые автомобили наносят больше ущерба воздуху, нежели новые, а во-вторых, нужно ведь
              куда-то девать старую машину, а не перегонять на мусорный полигон.
            </p>

