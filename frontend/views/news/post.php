            Суть проекта заключается в том, что автовладелец, имеющий автомобиль старше 10 лет, после сдачи ее на
            утилизацию получает специальный сертификат на сумму 50 000-350 000 рублей.

            С 2010 года в программе многое изменилось:

            <ol>
              <li>
                Сейчас деньги вручаются в виде субвенций регионам, которые сами выплачивают автозаводам денежные
                компенсации. Зависит это от результатов продаж года;
              </li>
              <li>
                В программе могут участвовать как физлица, так и юрлица (сюда же входят лизинговые компании);
              </li>
              <li>
                Помимо легковых автомобилей на утилизацию можно сдать автобусы и грузовики;
              </li>
              <li>Помимо легковых автомобилей на утилизацию можно сдать автобусы и грузовики;</li>
              <li>Появился принцип «Трейд-ин». Смысл принципа заключается в том, что машина сдается дилеру не просто на
                слом, а для перепродажи. Только есть один момент – машина, которая сдается по этой программе, должна
                быть не старше 6 лет. Этот автомобиль будет капитально отремонтирован и продан.
              </li>
